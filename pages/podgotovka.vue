        </p>
        <br />
        <p class="text-[14px] font-light">
          Накануне сдачи анализа рекомендуется не употреблять овощи и фрукты,
          которые могут изменить цвет мочи (свекла, морковь и пр.), не принимать
          диуретики.
        </p>
        <br />
        <p class="text-[14px] font-light">
          Перед сбором мочи надо произвести тщательный гигиенический туалет
          половых органов. Женщинам не рекомендуется сдавать анализ мочи во
          время менструации. Соберите примерно 50 мл утренней мочи в контейнер.
          Для правильного проведения исследования при первом утреннем
          мочеиспускании небольшое количество мочи (первые 1 - 2 сек.) выпустить
          в унитаз, а затем, не прерывая мочеиспускания, подставить контейнер
          для сбора мочи, в который собрать приблизительно 50 мл мочи.
        </p>
        <br />
        <p class="text-[14px] font-light">
          Сразу после сбора мочи плотно закройте контейнер завинчивающейся
          крышкой. Насадите пробирку вниз резиновой пробкой в углубление на
          крышке контейнера, при этом пробирка начнёт наполняться мочой. После
          того как моча прекратит поступать в пробирку, снимите пробирку с
          пробойника (рис. 1 и 2). Несколько раз переверните пробирку для
          лучшего смешивания мочи с консервантом (рис. 3).
        </p>
        <br />
        <p class="text-[14px] font-light">
          Доставить пробирку с мочой в медицинский офис необходимо в течение дня
          (по графику приёма биоматериала до 12:00). Если нет возможности сразу
          доставить мочу в медицинский офис, то пробирку с мочой следует хранить
          при температуре +2...+8°С.
        </p>
        <br />
        <span class="text-main text-[14px]"
          >Сбор суточной мочи для биохимического анализа</span
        ><br />
